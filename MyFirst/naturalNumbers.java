import java.lang.*;
import java.util.*;

public class naturalNumbers {
    public static void main(String[] args)
    {
        int i,num;
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter any number");
        
        num = sc.nextInt();
        
        for(i=0;i<=num;i++)
        {
            System.out.println(i);
        }
        
    }
    
}
